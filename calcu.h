#include <stdio.h>
#include <stdlib.h>


/** \brief calcular = pide los numeros x e y. Valida el llamado de cada operacion y Determina los resultado.
 *
 */
int calcular();

/** \brief sumar = realiza la suma
 *
 * \param numero1 float = variable x
 * \param numero2 float = variable y
 * \return float = retorna el valor de la suma
 *
 */
float sumar(float numero1, float numero2);

/** \brief restar = realiza la resta
 *
 * \param numero1 float = variable x
 * \param numero2 float = variable y
 * \return float = retorna el valor de la resta
 *
 */
float restar(float numero1, float numero2);

/** \brief multiplicar = realiza el producto
 *
 * \param numero1 float = variable x
 * \param numero2 float = variable y
 * \return float = retorna el valor del producto
 *
 */
float multiplicar(float numero1, float numero2);

/** \brief dividir = realiza el cociente
 *
 * \param numero1 float = variable x
 * \param numero2 float = variable y
 * \return float = retorna el valor del cociente
 *
 */
float dividir(float numero1, float numero2);

/** \brief ractoriar = calcula el factorial
 *
 * \param numero1 float = variable x
 * \return float = retorna el valor del factorial del numero.
 *
 */
float factoriar (float numero1);
